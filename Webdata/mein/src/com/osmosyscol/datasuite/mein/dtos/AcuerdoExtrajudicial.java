package com.osmosyscol.datasuite.mein.dtos;

public class AcuerdoExtrajudicial {

	private Integer id;
	private Integer idCarga;
	private String acuerdo_extrajudicial;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getIdCarga() {
		return idCarga;
	}
	public void setIdCarga(Integer idCarga) {
		this.idCarga = idCarga;
	}
	public String getAcuerdo_extrajudicial() {
		return acuerdo_extrajudicial;
	}
	public void setAcuerdo_extrajudicial(String acuerdo_extrajudicial) {
		this.acuerdo_extrajudicial = acuerdo_extrajudicial;
	}
	
}
