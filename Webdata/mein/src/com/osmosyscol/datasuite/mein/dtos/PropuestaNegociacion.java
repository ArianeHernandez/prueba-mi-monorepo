package com.osmosyscol.datasuite.mein.dtos;

public class PropuestaNegociacion {

	private Integer id;
	private Integer idCarga;
	private String propuesta_negociacion;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getIdCarga() {
		return idCarga;
	}
	public void setIdCarga(Integer idCarga) {
		this.idCarga = idCarga;
	}
	public String getPropuesta_negociacion() {
		return propuesta_negociacion;
	}
	public void setPropuesta_negociacion(String propuesta_negociacion) {
		this.propuesta_negociacion = propuesta_negociacion;
	}
	
}
