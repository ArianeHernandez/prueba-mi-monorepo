package com.osmosyscol.datasuite.bpm.dto;

public class ApVistaTramiteDto {
	private String idbpm;
	private String nombretramite;
	private String codigo;
	private String idpostal;
	
	public String getIdbpm() {
		return idbpm;
	}
	public void setIdbpm(String idbpm) {
		this.idbpm = idbpm;
	}
	public String getNombretramite() {
		return nombretramite;
	}
	public void setNombretramite(String nombretramite) {
		this.nombretramite = nombretramite;
	}
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getIdpostal() {
		return idpostal;
	}
	public void setIdpostal(String idpostal) {
		this.idpostal = idpostal;
	}
	
}
