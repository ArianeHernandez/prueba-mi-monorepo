package com.osmosyscol.datasuite.logica.dto;

public class ValorAtributo {

	private Integer id_administrativo;
	private Integer id_atributo;
	private String valor;

	public Integer getId_administrativo() {
		return id_administrativo;
	}

	public void setId_administrativo(Integer id_administrativo) {
		this.id_administrativo = id_administrativo;
	}

	public Integer getId_atributo() {
		return id_atributo;
	}

	public void setId_atributo(Integer id_atributo) {
		this.id_atributo = id_atributo;
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}

}
