package com.osmosyscol.datasuite.modelatos.logica.dto;


public class HistorialConsultaRol {

	private Integer id_historial_consulta_rol;
	private String nombre_columna;
	private String rol;
	private String valor;
	public Integer getId_historial_consulta_rol() {
		return id_historial_consulta_rol;
	}
	public void setId_historial_consulta_rol(Integer id_historial_consulta_rol) {
		this.id_historial_consulta_rol = id_historial_consulta_rol;
	}
	public String getNombre_columna() {
		return nombre_columna;
	}
	public void setNombre_columna(String nombre_columna) {
		this.nombre_columna = nombre_columna;
	}
	public String getRol() {
		return rol;
	}
	public void setRol(String rol) {
		this.rol = rol;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	
	
	
}
