package com.osmosyscol.datasuite.mein.dtos;

public class PaisDane {
	
	private Integer id;
	private String codigo;
	private String nombre;
	private Integer id_ssoc;
	private String nombre_ssoc;
	private Integer codigo_ssoc;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public Integer getId_ssoc() {
		return id_ssoc;
	}
	public void setId_ssoc(Integer id_ssoc) {
		this.id_ssoc = id_ssoc;
	}
	public String getNombre_ssoc() {
		return nombre_ssoc;
	}
	public void setNombre_ssoc(String nombre_ssoc) {
		this.nombre_ssoc = nombre_ssoc;
	}
	public Integer getCodigo_ssoc() {
		return codigo_ssoc;
	}
	public void setCodigo_ssoc(Integer codigo_ssoc) {
		this.codigo_ssoc = codigo_ssoc;
	}
	
}
