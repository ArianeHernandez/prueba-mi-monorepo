package com.osmosyscol.datasuite.mein.dtos;

public class ComunicacionAcreedores {

	private Integer id;
	private Integer idCarga;
	private String comunicacion_acreedores;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getIdCarga() {
		return idCarga;
	}
	public void setIdCarga(Integer idCarga) {
		this.idCarga = idCarga;
	}
	public String getComunicacion_acreedores() {
		return comunicacion_acreedores;
	}
	public void setComunicacion_acreedores(String comunicacion_acreedores) {
		this.comunicacion_acreedores = comunicacion_acreedores;
	}
	
}
