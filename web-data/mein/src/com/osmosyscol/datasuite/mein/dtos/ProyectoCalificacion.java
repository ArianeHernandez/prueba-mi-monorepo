package com.osmosyscol.datasuite.mein.dtos;

public class ProyectoCalificacion {

	private Integer id;
	private Integer idCarga;
	private String proyecto_calificacion;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getIdCarga() {
		return idCarga;
	}
	public void setIdCarga(Integer idCarga) {
		this.idCarga = idCarga;
	}
	public String getProyecto_calificacion() {
		return proyecto_calificacion;
	}
	public void setProyecto_calificacion(String proyecto_calificacion) {
		this.proyecto_calificacion = proyecto_calificacion;
	}
	
	
	
}
