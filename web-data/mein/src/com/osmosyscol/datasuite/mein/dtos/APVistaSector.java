package com.osmosyscol.datasuite.mein.dtos;

public class APVistaSector {

	private Integer id_vista;
	private String codigo_ciiu;
	private String descripcion;
	
	public Integer getId_vista() {
		return id_vista;
	}
	public void setId_vista(Integer id_vista) {
		this.id_vista = id_vista;
	}
	public String getCodigo_ciiu() {
		return codigo_ciiu;
	}
	public void setCodigo_ciiu(String codigo_ciiu) {
		this.codigo_ciiu = codigo_ciiu;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	
}
