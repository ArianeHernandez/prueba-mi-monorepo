package com.osmosyscol.datasuite.mein.dtos;

public class SupuestoAdmisibilidad {
	private Integer id;
	private Integer idCarga;
	private Integer supuestos_admisibilidad;
	private Integer supuestos_pnnc;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getIdCarga() {
		return idCarga;
	}
	public void setIdCarga(Integer idCarga) {
		this.idCarga = idCarga;
	}
	public Integer getSupuestos_admisibilidad() {
		return supuestos_admisibilidad;
	}
	public void setSupuestos_admisibilidad(Integer supuestos_admisibilidad) {
		this.supuestos_admisibilidad = supuestos_admisibilidad;
	}
	public Integer getSupuestos_pnnc() {
		return supuestos_pnnc;
	}
	public void setSupuestos_pnnc(Integer supuestos_pnnc) {
		this.supuestos_pnnc = supuestos_pnnc;
	}
	
}
