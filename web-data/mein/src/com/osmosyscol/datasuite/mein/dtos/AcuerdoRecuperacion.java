package com.osmosyscol.datasuite.mein.dtos;

public class AcuerdoRecuperacion {

	private Integer id;
	private Integer idCarga;
	private String acuerdo_recuperacion;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getIdCarga() {
		return idCarga;
	}
	public void setIdCarga(Integer idCarga) {
		this.idCarga = idCarga;
	}
	public String getAcuerdo_recuperacion() {
		return acuerdo_recuperacion;
	}
	public void setAcuerdo_recuperacion(String acuerdo_recuperacion) {
		this.acuerdo_recuperacion = acuerdo_recuperacion;
	}
	
}
