package com.osmosyscol.datasuite.logica.dto;

public class InstanciaAccion {

	private Integer id_accion;
	private Integer id_instancia;
	private Integer id_proceso_admin;
	private Integer id_administrativo;
	private Integer id_carga;
	
	public Integer getId_accion() {
		return id_accion;
	}
	public void setId_accion(Integer id_accion) {
		this.id_accion = id_accion;
	}
	public Integer getId_instancia() {
		return id_instancia;
	}
	public void setId_instancia(Integer id_instancia) {
		this.id_instancia = id_instancia;
	}
	public Integer getId_proceso_admin() {
		return id_proceso_admin;
	}
	public void setId_proceso_admin(Integer id_proceso_admin) {
		this.id_proceso_admin = id_proceso_admin;
	}
	public Integer getId_administrativo() {
		return id_administrativo;
	}
	public void setId_administrativo(Integer id_administrativo) {
		this.id_administrativo = id_administrativo;
	}
	public Integer getId_carga() {
		return id_carga;
	}
	public void setId_carga(Integer id_carga) {
		this.id_carga = id_carga;
	}

	
}
