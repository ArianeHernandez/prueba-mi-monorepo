package com.osmosyscol.datasuite.logica.dto;

public class CargaAccionAutomatica {

	private Integer id_carga;
	private Integer id_accion;
	private Integer id_instancia;
	private Integer id_lista_dinamica;

	public Integer getId_carga() {
		return id_carga;
	}

	public void setId_carga(Integer id_carga) {
		this.id_carga = id_carga;
	}

	public Integer getId_accion() {
		return id_accion;
	}

	public void setId_accion(Integer id_accion) {
		this.id_accion = id_accion;
	}

	public Integer getId_instancia() {
		return id_instancia;
	}

	public void setId_instancia(Integer id_instancia) {
		this.id_instancia = id_instancia;
	}

	public Integer getId_lista_dinamica() {
		return id_lista_dinamica;
	}

	public void setId_lista_dinamica(Integer id_lista_dinamica) {
		this.id_lista_dinamica = id_lista_dinamica;
	}

}
